// F3 Написать функцию и программу демонстрирующую работу данной функции.
// Вывести в порядке возрастания цифры, входящие в десятичную запись натурального числа N, не более 1000 цифр.
// Цифра, пробел, сколько раз данная цифра встречается в числе.
// Ввод: Натуральное число не более 1000 цифр. Пример ввода: 22443344
// Вывод: Цифры входящие в число через пробел в порядке возрастания. От самой младшей цифры до самой старшей и количество.
//Пример вывода:
//2 2
//3 2
//4 4


#include <stdio.h>

void count_and_print_digits(const char *number) {
    int counts[10] = {0};

    for (int i = 0; number[i] != '\0'; i++) {
        if (number[i] >= '0' && number[i] <= '9') {
            counts[number[i] - '0']++;
        }
    }

    for (int i = 0; i < 10; i++) {
        if (counts[i] > 0) {
            printf("%d %d\n", i, counts[i]);
        }
    }
}

int main() {
    char number[1002];
    int i = 0;
    int ch;

    while ((ch = getchar()) != EOF && ch != '\n' && ch != '\r' && i < 1000) {
        if (ch >= '0' && ch <= '9') {
            number[i++] = ch;
        }
    }
    number[i] = '\0';

    if (i > 0) {
        count_and_print_digits(number);
    }

    return 0;
}
