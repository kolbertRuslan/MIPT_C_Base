//Формат входных данных
//Строка из английских букв, пробелов, знаков препинания и цифр
//Формат результата
//Функция должна принмать на вход строку и выводить на печать по формату: Цифра пробел количество.

void print_digit(char s[]) {
    int counts[10] = {0};

    // Подсчет частоты каждой цифры
    for (int i = 0; s[i] != '\0'; i++) {
        if (s[i] >= '0' && s[i] <= '9') {
            counts[s[i] - '0']++;
        }
    }

    // Вывод цифр в порядке возрастания (от 0 до 9)
    for (int i = 0; i <= 9; i++) {
        if (counts[i] > 0) {
            printf("%d %d\n", i, counts[i]);
        }
    }
}